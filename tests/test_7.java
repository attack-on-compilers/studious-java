// Java Program to Illustrate Do-while Loop

// Class
class GFG {

	// Main driver method
	public static void main(String args[])
	{

		// Declaring and initialization expression
		int i = 1;

		// Do-while loop
		do {

			// Body of do-while loop
			// Print statement
			System.out.println("Hello World");

			// Update expression
			i++;
		}

		// Test expression
		while (i < 6);
	}
}
